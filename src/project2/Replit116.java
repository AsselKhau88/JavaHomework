package project2;

public class Replit116 {

        static boolean numbers(int a, int b) {
            if (a % 2 == 0 && b % 2 == 0) {
                return true;
            } else {
                return false;

            }
        }

        public static void main(String[] args) {
            System.out.println(numbers(6, 8));
        }

    }


