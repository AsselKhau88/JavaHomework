package class7;

public class E7WhileLoop {
    public static void main(String[] args) {
        // print these number 2 4 6 8 10 12 14 using a while loop
         int counter=2;
         while(counter<14){
             // below line will increase the value of the counter by 2 everything
             counter+=2; // counter=counter+2 or counter+=2 counter+=2

             System.out.println(counter);

         }
    }
}
