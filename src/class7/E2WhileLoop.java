public class E2WhileLoop {
    public static void main(String[] args) {

        int number=0;
        // below we are using the increment operator( it increases value by 1)
        number++; // 0-> 1
        number++; //1-> 2
        System.out.println(number);
         // below we are using to decrement operator ( it decreases by 1)
         int num2=10;
         num2--;
        System.out.println(num2);


    }
}
