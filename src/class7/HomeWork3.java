package class7;

public class HomeWork3 {
    //Print even numbers from 20 to 1 (2 ways)
    public static void main(String[] args) {
        int i=20;
        while (i>=1){
            System.out.print(i+" ");
            i-=2;
        }
        for (int j = 20; j >=1; j-=2) {
            System.out.print(j+" ");

        }




    }
}
