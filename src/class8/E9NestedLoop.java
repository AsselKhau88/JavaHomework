package class8;

public class E9NestedLoop {
    public static void main(String[] args) {
        // in line below j<100 represents the number of lines
        for (int j = 0; j < 100; j++) {
            //line below i<5 represents number of stars to be printed
            for (int i = 0; i < 5; i++) {
                System.out.print("* ");

            }
            System.out.println();
        }
    }
}
