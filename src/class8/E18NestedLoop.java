package class8;

public class E18NestedLoop {
    public static void main(String[] args) {
        System.out.print("1 ");
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        // Second line
        System.out.print("2 ");
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
    }
}
